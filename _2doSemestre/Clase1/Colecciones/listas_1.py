#Las listas son un conjunto de elementos

nombres = ['Naty', 'Osvaldo','Lily', 'Ariel']
print(nombres)  #Esto muestra la lista completa
print(nombres[0])   # Esto muestra el elemento 0 en la lista, se lee de izquierda a derecha
print(nombres[3])   # Esto muestra el ultimo elemento, se comienza en el cero y de ahi se cuentan los elementos dentro.
print(nombres[-1])  # Esto muestra de forma inversa, con numeros negativos 
print(nombres[-2])  # Pero no puede comenzar con el -0 (cero negativo)