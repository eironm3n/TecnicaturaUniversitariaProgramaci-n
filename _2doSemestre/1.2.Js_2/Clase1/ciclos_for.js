// for

for ( let contando = 0; contando < 3 ; contando++ ){
    console.log(contando);
}
/* 
en este caso, en la primer parte establecemos lo que se pide dentro del for
en la segunda parte luego del punto y coma, establecemos la condicion
y por ultimo, en la ultima parte, para que vaya incrementando y continuando
esto es como el ciclo while y do while, pero mas resumido
*/
console.log("Fin del ciclo for")
