# Profundizando en el tipo float
a = 3.0
print(f'a= {a:.2f}')

# Constructor de tipo float -> puede recibir int y str
b = float(10) # le pasamos un tipo entero (int)
print(f'b= {b:.2f}')

# Notacion exponencial (valores positivos o negativos)
c = 3e5
print(f'c= {c:.2f}')
