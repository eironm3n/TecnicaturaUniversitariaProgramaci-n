""" Ejercicio: El mayor de dos numeros
Solicitar al usuario dos valores, determinar cual es el mayor"""

num1=int(input("Digite un numero para Numero 1: "))
num2=int(input("Digite un numero para Numero 2: "))

if num1 > num2:
    print("El numero 1 es el mayor")
else:
    print("El numero 2 es el mayor")